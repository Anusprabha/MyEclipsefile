package myjavaprograms;

public class Operator_Assignment {

	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		
		a+=3;//a=a+3 (10+3)
		System.out.println(a);
		
		b-=4;//b=b-4 (20-4)
		System.out.println(b);
		
		a-=4;//a=a-4 (13-4)
		System.out.println(a);
		
		a*=2;//a=a*2 (9*2)
		System.out.println(a);
		
		a/=2;//a=a/2 (18/2)
		System.out.println(a);
		

	}

}

